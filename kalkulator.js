// Operasi Penambahan
function tambah(a, b) {
    return a + b;
}
console.log("Hasil penambahan: " + tambah(5, 3));

// Operasi Pengurangan
function kurang(a, b) {
    return a - b;
}
console.log("Hasil pengurangan: " + kurang(5, 3));

// Operasi Perkalian
function kali(a, b) {
    return a * b;
}
console.log("Hasil perkalian: " + kali(5, 3));
